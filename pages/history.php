    <h1>Histoire de la ville</h1>
    <br/>
    <p><strong>Arras, située dans le Pas-de-Calais, possède une histoire riche marquée par des événements clés et des périodes de prospérité et de conflits.</strong></p>
    <br/>
    <h5>1. Antiquité et époque romaine :</h5>
    <p>Initialement un village gaulois, Arras devient un centre stratégique sous l’Empire romain, facilitant les échanges commerciaux.</p>
    <br/><br/>

    <h5>2. Moyen Âge :</h5>
    <p>La ville se développe en un important centre commercial, notamment dans l’industrie de la laine et des tapisseries.</p>
    <br/><br/>

    <h5>3. Guerre de Cent Ans :</h5>
    <p>Arras est marquée par les conflits entre la France et l’Angleterre. Le traité d’Arras en 1435 met fin à la guerre entre la France et le duché de Bourgogne.</p>
    <br/><br/>

    <h5>4. Renaissance :</h5>
    <p>La ville prospère grâce au commerce, tout en étant influencée par la culture flamande et bourguignonne. Les guerres de religion compliquent cette période.</p>
    <br/><br/>

    <h5>5. Révolution française et XIXe siècle :</h5>
    <p>Arras joue un rôle important lors de la Révolution. Le XIXe siècle voit une modernisation de la ville avec de nouvelles infrastructures.</p>
    <br/><br/>

    <h5>6. Première Guerre mondiale :</h5>
    <p>La ville subit de lourdes destructions. Après la guerre, elle est reconstruite, et des monuments comme le Beffroi et la Basilique Saint-Vaast sont classés au patrimoine mondial de l'UNESCO.</p>
    <br/><br/>

    <h5>7. Aujourd'hui :</h5>
    <p>Arras est une ville dynamique et culturelle, attirant les visiteurs grâce à son riche patrimoine et ses événements comme le Main Square Festival.</p>
    <br/><br/>

    <p>Elle combine tradition et modernité, tout en restant un lieu de mémoire des événements du XXe siècle, notamment la Première Guerre mondiale.</p>
