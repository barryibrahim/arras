    <h1>Histoire de la ville</h1>
    <br/>
    <p><strong>Arras est une ville située dans le département du Pas-de-Calais, dans le nord de la France. Elle possède une histoire riche, façonnée par des événements marquants et des périodes<br/> variées de prospérité et de conflits. Voici un aperçu de cette histoire fascinante :</strong></p>
    <br/>
    <h5>1. Antiquité et époque romaine :</h5>
    <p>Arras était initialement un village gaulois habité par les Atrebates, un peuple celte. Lors de la conquête romaine au Ier siècle avant J.-C., </br>la ville devient un centre stratégique de la province romaine de la Belgica, sous le nom de Atuatuca. C'était une ville commerçante dynamique, idéale pour les échanges entre les peuples.</p>
    <br/><br/>

    <h5>2. Moyen Âge :</h5>
    <p>Au Moyen Âge, la ville bénéficie de sa position géographique stratégique et devient un pôle commercial important. Au XIe siècle, <br/>elle se distingue pour son industrie de la laine et des tapisseries, qui rencontrent un grand succès en Europe. La ville connaît également une grande prospérité religieuse et politique.</p>
    <br/><br/>

    <h5>3. Guerre de Cent Ans et période de troubles :</h5>
    <p>Au XIVe siècle, la guerre de Cent Ans entre la France et l'Angleterre touche directement Arras. <br/>La ville passe plusieurs fois sous le contrôle des Anglais avant d’être récupérée par les Français. En 1435, <br/>Arras devient le lieu de signature du traité du même nom, un accord majeur mettant fin au conflit entre le royaume de France et le duché de Bourgogne.</p>
    <br/><br/>

    <h5>4. Renaissance et domination bourguignonne :</h5>
    <p>Durant la Renaissance, la ville prospère grâce à son commerce florissant, notamment de tissus et de tapisseries. <br/>Influencée par les cultures flamande et bourguignonne, Arras traverse une période plus agitée durant les guerres de religion au XVIe siècle.</p>
    <br/><br/>

    <h5>5. Révolution française et XIXe siècle :</h5>
    <p>Au XVIIIe siècle, Arras, comme de nombreuses autres villes françaises, traverse une période de tensions sociales et politiques, culminant avec la Révolution française. <br/>La ville devient un centre d'agitation révolutionnaire. Au XIXe siècle, Arras se modernise avec la construction de nouveaux bâtiments et infrastructures.</p>
    <br/><br/>

    <h5>6. Première Guerre mondiale :</h5>
    <p>La Première Guerre mondiale constitue un tournant pour Arras, située à proximité du front. <br/>Elle est lourdement bombardée et subit des destructions massives. Les tranchées et les combats violents marquent profondément la région. <br/>Après la guerre, la ville est reconstruite, et des monuments emblématiques tels que le Beffroi et la Basilique Saint-Vaast sont restaurés et classés au patrimoine mondial de l'UNESCO.</p>
    <br/><br/>

    <h5>7. Aujourd'hui :</h5>
    <p>Aujourd'hui, Arras est une ville vivante et culturellement riche, attirant les visiteurs grâce à son patrimoine historique. On y trouve des sites comme la Grand-Place, <br/>les Places des Héros, le Beffroi, ainsi que des musées remarquables. La ville est aussi le lieu de nombreux événements, comme le Main Square Festival, qui célèbrent la culture et la diversité.</p>
    <br/><br/>

    <p>Arras continue de vivre au rythme de son histoire, alliant tradition et modernité, <br/>et reste un lieu de mémoire, en particulier pour les événements marquants du XXe siècle, notamment la Première Guerre mondiale.</p>
