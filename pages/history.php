    <br/><br/><h1>Histoire de la ville</h1><br/>
    <p>Arras a une histoire extrêmement riche.<br/>
    La ville d'Arras est située dans le département du Pas-de-Calais, dans le nord de la France. Elle a une histoire riche et elle a été un centre commercial important durant le Moyen âge et a joué un rôle clé durant les guerres mondiales.<br/>
    La ville est également connue pour ses magnifiques places et son architecture.</p>
