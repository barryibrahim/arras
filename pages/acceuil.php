    <h2>Acceuil</h2>
    <p>Bienvenue à Arras, joyau des Hauts-de-France</p>
    <p>Découvrez Arras, une ville au riche patrimoine historique et à l’atmosphère chaleureuse. Célèbre pour ses magnifiques places baroques, son beffroi classé à l’UNESCO et ses souterrains chargés d’histoire, Arras est une destination incontournable pour les amoureux de culture d’architecture et de gastronomie.</p>
    <p>ue vous soyez ici pour une visite, un séjour ou simplement par curiosité, notre site vous guide à travers les trésors de la ville : événements, monuments, bonnes adresses et anecdotes historiques.</p>
