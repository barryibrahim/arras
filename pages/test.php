     <h1>La Gastronomie à Arras</h1>

    <main>
        <section>
            <h2>Introduction</h2>
            <p>Arras, célèbre pour ses magnifiques places et son beffroi classé au patrimoine mondial de l'UNESCO, est également une destination incontournable pour les amateurs de gastronomie. La ville regorge de spécialités locales et de restaurants qui mettent en valeur les produits du terroir.</p>
        </section>
        <section>
            <h2>Spécialités culinaires</h2>
            <ul>
                <li><strong>Andouillette d'Arras :</strong> Une charcuterie traditionnelle à base de porc, souvent servie grillée avec une sauce moutarde.</li>
                <li><strong>Coeur d'Arras :</strong> Un délicieux fromage local à pâte molle et à croûte lavée, parfait pour accompagner un bon pain.</li>
                <li><strong>Les rissoles :</strong> De petits chaussons feuilletés, garnis de viande ou de fromage.</li>
                <li><strong>Les bêtises de Cambrai :</strong> Bien que provenant d'une ville voisine, ces bonbons sont souvent présents dans les boutiques d'Arras.</li>
            </ul>
        </section>
        <section>
            <h2>Restaurants recommandés</h2>
            <p>Voici quelques établissements à découvrir lors de votre visite :</p>
            <ul>
                <li><strong>Le Clusius :</strong> Un restaurant gastronomique réputé pour ses plats raffinés et son ambiance élégante.</li>
                <li><strong>Le Comptoir à Pizzas :</strong> Idéal pour une pause conviviale avec des pizzas préparées à partir d'ingrédients locaux.</li>
                <li><strong>Meert :</strong> Une institution pour les amateurs de gaufres et de douceurs sucrées.</li>
            </ul>
        </section>
        <section>
            <h2>Événements gastronomiques</h2>
            <p>Arras organise régulièrement des événements culinaires, comme le marché de Noël, où vous pouvez déguster des produits régionaux, ou encore des foires gastronomiques mettant en avant les artisans locaux.</p>
        </section>
