    <h2>Monuments et Lieux d'Intérêt</h2>
    <p>Découvrez les monuments représentatifs.</p>
    <p>Arras est célèbre pour ses monuments historiques, dont la magnifique place des Héros, bordée de maisons en sytle.</p>
    <p>Les visiteurs peuvent découvrir l'Abbaye Saint-Vaast, un ancien monastère devenu musée, ainsi que les superbes caves de la ville, qui offrent un aperçu de l'histoire viticole de la région.</p>
    <p>Parmi les autres sites incontournables, on trouve la Citadelle d'Arras et la Maison de la Culture d'Arras, qui propose une programmation culturelle variée.</p>
