<section id="events">
    <br/><br/><h1 style="text-align: center;">Événements culturels</h1><br/>
    <p style="text-align: center;"><strong>Arras accueille de nombreux événements majeurs tels que le Festival Arras Film, le Main Square Festival et le Marché de Noël, attirant des milliers de visiteurs chaque année.</strong></p>
    <br/>

    <h3 style="text-align: center;">Le Festival Arras Film :</h3>
    <img src="../public/images/arrasfilm.jpg" alt="Festival Arras Film" width="900" height="400" style="display: block; margin: 0 auto;">
    <p style="text-align: center;">
        Le Festival Arras Film est un événement cinématographique majeur mettant en avant le cinéma européen. Avec une programmation variée, des films récents et des avant-premières, il attire cinéphiles et professionnels. C'est un rendez-vous incontournable pour découvrir des films en version originale et participer à des conférences.
    </p> 
    <br/>

    <h3 style="text-align: center;">Le Main Square :</h3>
    <img src="../public/images/mainsquare.png" alt="Main Square Festival" width="600" height="400" style="display: block; margin: 0 auto;">
    <p style="text-align: center;">
        Le Main Square Festival, organisé chaque année en juin, est un événement musical majeur. Il attire des milliers de spectateurs grâce à sa programmation variée, mêlant rock, électro et pop, dans le cadre exceptionnel de la Grand-Place d'Arras.
    </p>
    <br/>

    <h3 style="text-align: center;">Le Marché de Noël :</h3>
    <img src="../public/images/marchedenoel.jpg" alt="Marché de Noël" width="600" height="400" style="display: block; margin: 0 auto;">
    <p style="text-align: center;">
        Le Marché de Noël d'Arras transforme la ville en un village féérique avec ses chalets décorés et ses illuminations. Il propose une variété de produits artisanaux, des spécialités locales et des animations pour toute la famille, créant une atmosphère festive idéale pour les fêtes de fin d'année.
    </p>
</section>
