    </figure>
    <p class="text-center">
        <br/>
        La Citadelle d'Arras, un exemple d'architecture militaire datant du XVIIe siècle.<br/>
        La Citadelle d'Arras, datant du XVIIe siècle, est un exemple exceptionnel d'architecture militaire. Elle est située sur une colline et offre une vue imprenable sur la ville et ses environs. <br/>
        C'est un site historique fascinant, riche en histoire et en patrimoine.
    </p>
