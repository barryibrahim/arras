<section id="monuments">
    </br><br/><h1 class="text-center">Monuments et Lieux d'Intérêt</h1><br/>
    <p class="text-center"><strong>Découvrez les monuments emblématiques d'Arras, célèbres pour leur richesse historique.</strong></p>
    <br/>

    <h3 class="text-center">La Place des Héros :</h3>
    <figure class="text-center">
        <img src="../public/images/grandeplace.webp" alt="La Place des Héros à Arras, un lieu emblématique entouré de bâtiments historiques" width="900" height="400">
    </figure>
    
    <p class="text-center">
        La Place des Héros est l'un des sites les plus emblématiques d'Arras, entourée de bâtiments historiques. Elle est un lieu de rassemblement pour les habitants et visiteurs, idéale pour explorer la ville ou se détendre.
    </p>
    <br/>

    <h3 class="text-center">L'Abbaye Saint-Vaast :</h3>
    <figure class="text-center">
        <img src="../public/images/AbbayeSaintVaast.jpg" alt="L'Abbaye Saint-Vaast à Arras, un ancien monastère devenu musée" width="600" height="400">
    </figure>
    <p class="text-center">
        L'Abbaye Saint-Vaast, ancien monastère devenu musée, permet de découvrir l'histoire de la ville d'Arras. Les superbes caves de l'abbaye offrent un aperçu de l'histoire viticole locale.
    </p>
    <br/>

    <h3 class="text-center">La Citadelle d'Arras :</h3>
    <figure class="text-center">
        <img src="../public/images/citadelle.jpg" alt="La Citadelle d'Arras, un exemple d'architecture militaire datant du XVIIe siècle" width="600" height="400">
    </figure>
    <p class="text-center">
        La Citadelle d'Arras, datant du XVIIe siècle, est un exemple d'architecture militaire offrant une vue panoramique sur la ville. C'est un site chargé d'histoire et de patrimoine.
    </p>
